##########
# 9. Figure 4: biallelic-only breaks down as theta (and multiallelic density) rises,
#    multiallelic-aware stays on its finite-sites expectation
##########
# pi/dxy averaged over windows per replicate; E[stat](theta) read from the figure's
# summaries to match the plotted dashed line (E depends on theta only). Hudson FST is
# a ratio of sums, taken from the ratio-correct summary not averaged per-window ratios.
# figure plots 2n/8n; theta trend and its ploidy dependence reported over the full sweep
emit("\n\n## 9. Figure 4 - biallelic breakdown vs multiallelic-aware (theta sweep)\n")
theta_codes4 <- c("005" = 0.005, "010" = 0.01, "025" = 0.025, "050" = 0.05, "100" = 0.1)
pi_sum4   <- tryCatch(read_tsv(file.path(ANL, "multiallelic_frac_summary.tsv"),
                               show_col_types = FALSE, progress = FALSE), error = function(e) NULL)
dxyf_sum4 <- tryCatch(read_tsv(file.path(ANL, "dxy_fst_theta_summary.tsv"),
                               show_col_types = FALSE, progress = FALSE), error = function(e) NULL)
if (is.null(pi_sum4) || is.null(dxyf_sum4)) {
    emit("\n_(missing multiallelic_frac_summary.tsv or dxy_fst_theta_summary.tsv)_\n")
} else {
    Emap <- function(tb) setNames(tb$theoretical, sprintf("%.3f", tb$theta_nominal))
    E_pi_v  <- Emap(distinct(pi_sum4, theta_nominal, theoretical))
    E_dxy_v <- Emap(distinct(filter(dxyf_sum4, stat == "dxy"), theta_nominal, theoretical))
    # FST summary carries two expectations (infinite-sites biallelic, finite-sites
    # multi). here both estimators judged against the same finite-sites E, matching
    # pi/dxy and Figure 4; that common target is what makes the comparison meaningful.
    # section 5 runs the complementary test (each vs its own target).
    # theoretical_fst_finite depends only on theta, so one value per theta
    E_fst_v <- Emap(distinct(filter(dxyf_sum4, stat == "fst_hudson",
                                    estimator == "new_multi"),
                             theta_nominal, theoretical))
    stopifnot(length(E_fst_v) == length(unique(dxyf_sum4$theta_nominal)))

    # per-replicate relative bias (%) across the ploidy x theta sweep
    sweep_rel <- function(prefix, suffix, cols, Ev) {
        map_dfr(names(theta_ploidy_files), function(k) {
            map_dfr(names(theta_codes4), function(tc) {
                d <- read_arm(paste0(prefix, theta_ploidy_files[k], suffix, tc))
                if (is.null(d)) return(NULL)
                th <- unname(theta_codes4[tc]); E <- unname(Ev[sprintf("%.3f", th)])
                d |> group_by(replicate) |>
                    summarise(bi = mean(.data[[cols[1]]], na.rm = TRUE),
                              multi = mean(.data[[cols[2]]], na.rm = TRUE), .groups = "drop") |>
                    mutate(ploidy = theta_ploidy_n[k], theta = th,
                           rbi = 100 * (bi - E) / E, rmu = 100 * (multi - E) / E)
            })
        })
    }

    report_sweep <- function(sw, label) {
        if (nrow(sw) == 0) { emit("\n_(no %s sweep data)_\n", label); return(invisible()) }
        cell_bi <- sw |> group_by(ploidy, theta) |> summarise(b = mean(rbi), .groups = "drop")
        cell_mu <- sw |> group_by(ploidy, theta) |> summarise(b = mean(rmu), .groups = "drop")
        thmin <- min(sw$theta); thmax <- max(sw$theta)
        pls <- sort(unique(cell_bi$ploidy))
        g <- function(tb, pl, th) tb$b[tb$ploidy == pl & abs(tb$theta - th) < 1e-9]
        fit <- lm(b ~ theta, data = cell_bi); cf <- confint(fit)
        w <- which.min(cell_bi$b); cmu <- ci(sw$rmu)
        emit("\n**%s.** Biallelic-only ('new') relative bias vs the finite-sites expectation:\n", label)
        emit("\n- theta = %.3f: %+.2f%% (2n), %+.2f%% (8n) -> theta = %.2f: %+.2f%% (2n), %+.2f%% (8n)",
             thmin, g(cell_bi, 2, thmin), g(cell_bi, 8, thmin),
             thmax, g(cell_bi, 2, thmax), g(cell_bi, 8, thmax))
        emit("\n- worst cell: %+.2f%% (%dn, theta = %.2f)", cell_bi$b[w], cell_bi$ploidy[w], cell_bi$theta[w])
        emit("\n- monotone with theta (all ploidies): OLS slope = %+.1f%% per unit theta [%.1f, %.1f], p = %s",
             coef(fit)["theta"], cf["theta", 1], cf["theta", 2],
             format.pval(summary(fit)$coefficients["theta", "Pr(>|t|)"], digits = 2))
        emit("\n- worsens with ploidy at theta = %.2f: %s", thmax,
             paste(sprintf("%dn %+.1f%%", pls, sapply(pls, function(p) g(cell_bi, p, thmax))), collapse = ", "))
        emit("\n\nMultiallelic-aware ('new_multi') relative bias: pooled %+.2f%% [%+.2f%%, %+.2f%%]; max |cell| = %.2f%% across the whole sweep.\n",
             cmu$mean, cmu$lo, cmu$hi, max(abs(cell_mu$b)))
    }

    pi_sw  <- sweep_rel("pi_",  "_1pop_theta", c("pi_new", "pi_new_multi"),   E_pi_v)
    dxy_sw <- sweep_rel("dxy_", "_2pop_theta", c("dxy_new", "dxy_new_multi"), E_dxy_v)
    report_sweep(pi_sw,  "pi")
    report_sweep(dxy_sw, "dxy")

    # factorial ANOVA of the biallelic deficit: does relative deviation depend on
    # statistic (pi vs dxy), ploidy, theta? theta/ploidy as factors (5 and 4 levels),
    # balanced at 2,000 reps/cell. Figure-4 analogue of the Figure-3 combined ANOVA
    if (nrow(pi_sw) && nrow(dxy_sw)) {
        fig4_bi <- bind_rows(
            pi_sw  |> transmute(statistic = "pi",  ploidy, theta, rel_dev = rbi),
            dxy_sw |> transmute(statistic = "dxy", ploidy, theta, rel_dev = rbi)) |>
            filter(is.finite(rel_dev)) |>
            mutate(statistic = factor(statistic, levels = c("pi", "dxy")),
                   ploidy_f  = factor(ploidy), theta_f = factor(theta))
        aov4 <- aov(rel_dev ~ statistic * ploidy_f * theta_f, data = fig4_bi)
        at4 <- summary(aov4)[[1]]
        ss4 <- sum(at4[["Sum Sq"]]); tn4 <- trimws(rownames(at4))
        pn4 <- gsub("ploidy_f", "ploidy", gsub("theta_f", "theta", gsub(":", " x ", tn4)))
        emit("\n**Biallelic-only deficit - factorial ANOVA** (relative deviation from theory (%%) ~")
        emit(" statistic x ploidy x theta, all factors; N = %d replicate estimates, 2,000 per cell).\n", nrow(fig4_bi))
        emit("\n| term | df | F | p | %% of total SS |")
        emit("\n|---|---|---|---|---|")
        for (i in seq_along(tn4)) {
            if (pn4[i] == "Residuals") next
            emit("\n| %s | %d | %.1f | %s | %.1f%% |",
                 pn4[i], at4[["Df"]][i], at4[["F value"]][i],
                 format.pval(at4[["Pr(>F)"]][i], digits = 2), 100 * at4[["Sum Sq"]][i] / ss4)
        }
        ri <- length(tn4)
        emit("\n| residual | %d | | | %.1f%% |\n", at4[["Df"]][ri], 100 * at4[["Sum Sq"]][ri] / ss4)
    }

    # same ANOVA on the multiallelic-aware estimator: claim is its deviation shows
    # no relationship with theta, ploidy, or statistic. same model and N as the
    # biallelic ANOVA, so F differences reflect effect size not power
    if (nrow(pi_sw) && nrow(dxy_sw)) {
        fig4_mu <- bind_rows(
            pi_sw  |> transmute(statistic = "pi",  ploidy, theta, rel_dev = rmu),
            dxy_sw |> transmute(statistic = "dxy", ploidy, theta, rel_dev = rmu)) |>
            filter(is.finite(rel_dev)) |>
            mutate(statistic = factor(statistic, levels = c("pi", "dxy")),
                   ploidy_f  = factor(ploidy), theta_f = factor(theta))
        aov4m <- aov(rel_dev ~ statistic * ploidy_f * theta_f, data = fig4_mu)
        at4m <- summary(aov4m)[[1]]
        ss4m <- sum(at4m[["Sum Sq"]]); tn4m <- trimws(rownames(at4m))
        pn4m <- gsub("ploidy_f", "ploidy", gsub("theta_f", "theta", gsub(":", " x ", tn4m)))
        emit("\n**Multiallelic-aware estimator - factorial ANOVA** (relative deviation from")
        emit(" theory (%%) ~ statistic x ploidy x theta, all factors; N = %d replicate", nrow(fig4_mu))
        emit(" estimates, 2,000 per cell - identical design and N to the biallelic ANOVA above).\n")
        emit("\n| term | df | F | p | %% of total SS |")
        emit("\n|---|---|---|---|---|")
        for (i in seq_along(tn4m)) {
            if (pn4m[i] == "Residuals") next
            emit("\n| %s | %d | %.2f | %s | %.2f%% |",
                 pn4m[i], at4m[["Df"]][i], at4m[["F value"]][i],
                 format.pval(at4m[["Pr(>F)"]][i], digits = 2), 100 * at4m[["Sum Sq"]][i] / ss4m)
        }
        rim <- length(tn4m)
        emit("\n| residual | %d | | | %.2f%% |\n", at4m[["Df"]][rim], 100 * at4m[["Sum Sq"]][rim] / ss4m)
        nonsig <- sum(at4m[["Pr(>F)"]][-rim] > 0.05, na.rm = TRUE)
        emit("\n%d / %d terms are non-significant at alpha = 0.05; the model explains %.2f%% of the",
             nonsig, rim - 1, 100 * (ss4m - at4m[["Sum Sq"]][rim]) / ss4m)
        emit(" variance in deviation-from-theory (residual = %.1f%%).\n",
             100 * at4m[["Sum Sq"]][rim] / ss4m)
    }

    # Hudson FST is a ratio of within/between diversity, so the exclusion that costs
    # pi/dxy double-digit percentages at high theta costs FST ~0.3% (numerator and
    # denominator move alike and cancel). each estimator vs its own `theoretical`
    # (biallelic -> infinite-sites, multi -> finite-sites). see section 5
    fstf <- dxyf_sum4 |>
        filter(stat == "fst_hudson", estimator %in% c("new", "new_multi")) |>
        mutate(contains_E = q025 <= theoretical & q975 >= theoretical)
    emit("\n**Hudson FST.** A ratio of sums, so the biallelic downward bias largely")
    emit(" cancels between numerator and denominator; each estimator tracks its own")
    emit(" expectation across the sweep:\n")
    for (est in c("new", "new_multi")) {
        x <- fstf |> filter(estimator == est)
        if (nrow(x) == 0) next
        lab <- if (est == "new") "biallelic (vs infinite-sites E[FST] = 0.237)" else
                                 "multiallelic-aware (vs finite-sites E[FST](theta))"
        emit("\n- %s: max |bias| = %.2f%% over %d cells; across-replicate 95%% interval contains E[FST] in %d / %d cells",
             lab, max(abs(x$bias_pct)), nrow(x), sum(x$contains_E), nrow(x))
    }
    emit("\n")

    # Hudson FST factorial ANOVA: is deviation from each estimator's own E[FST]
    # related to estimator, theta, ploidy? two real FST estimators now: before the
    # 2026-07 fix pixy's FST path re-filtered to biallelic regardless of
    # --include_multiallelic_snps, so both columns held the biallelic estimand; as of
    # 02d6d91 `new_multi` is a real multiallelic FST across every allele column.
    # rel_dev vs the finite-sites E for both estimators (matching pi/dxy and Figure 4),
    # so the estimator term means "cost of discarding multiallelic sites". section 5
    # runs each vs its own target. per-replicate FST = pooled ratio-of-sums
    # (sum num / sum den over the rep's windows); averaging per-window ratios is
    # Jensen-biased downward
    fst_rep <- map_dfr(names(theta_ploidy_files), function(k) {
        map_dfr(names(theta_codes4), function(tc) {
            d <- read_arm(paste0("dxy_", theta_ploidy_files[k], "_2pop_theta", tc))
            if (is.null(d)) return(NULL)
            map_dfr(c("new", "new_multi"), function(est) {
                nc <- paste0("fst_hudson_num_", est)
                dc <- paste0("fst_hudson_den_", est)
                if (!all(c(nc, dc) %in% names(d))) return(NULL)
                d |>
                    select(replicate, num = all_of(nc), den = all_of(dc)) |>
                    filter(is.finite(num), is.finite(den)) |>
                    group_by(replicate) |>
                    summarise(value = sum(num) / sum(den), .groups = "drop") |>
                    mutate(ploidy = theta_ploidy_n[k], theta = unname(theta_codes4[tc]),
                           estimator = est)
            })
        })
    })
    if (nrow(fst_rep)) {
        # one expectation per theta (finite-sites) for both estimators, matching
        # sweep_rel() and Figure 4. see the note at E_fst_v
        fst_rep <- fst_rep |>
            mutate(theoretical = unname(E_fst_v[sprintf("%.3f", theta)]))
        # a missing lookup would silently NA every row and empty the model
        stopifnot(!any(is.na(fst_rep$theoretical)))
        fst_rep <- fst_rep |>
            mutate(rel_dev = 100 * (value - theoretical) / theoretical) |>
            filter(is.finite(rel_dev)) |>
            mutate(estimator_f = factor(estimator, levels = c("new", "new_multi")),
                   ploidy_f = factor(ploidy), theta_f = factor(theta))
        aovf <- aov(rel_dev ~ estimator_f * ploidy_f * theta_f, data = fst_rep)
        atf <- summary(aovf)[[1]]
        ssf <- sum(atf[["Sum Sq"]]); tnf <- trimws(rownames(atf))
        pnf <- gsub("estimator_f", "estimator",
                    gsub("ploidy_f", "ploidy",
                         gsub("theta_f", "theta", gsub(":", " x ", tnf))))
        emit("\n**Hudson FST - factorial ANOVA** (relative deviation from the finite-sites")
        emit(" E[FST] (%%) ~ estimator x ploidy x theta, all factors; both estimators judged")
        emit(" against the same expectation, as for pi and dxy above and as plotted in")
        emit(" Figure 4; per-replicate FST = pooled ratio-of-sums; N = %d replicate",
             nrow(fst_rep))
        emit(" estimates).\n")
        emit("\n| term | df | F | p | %% of total SS |")
        emit("\n|---|---|---|---|---|")
        for (i in seq_along(tnf)) {
            if (pnf[i] == "Residuals") next
            emit("\n| %s | %d | %.2f | %s | %.2f%% |",
                 pnf[i], atf[["Df"]][i], atf[["F value"]][i],
                 format.pval(atf[["Pr(>F)"]][i], digits = 2), 100 * atf[["Sum Sq"]][i] / ssf)
        }
        rif <- length(tnf)
        emit("\n| residual | %d | | | %.2f%% |\n", atf[["Df"]][rif], 100 * atf[["Sum Sq"]][rif] / ssf)
        for (est in c("new", "new_multi")) {
            x <- fst_rep |> filter(estimator == est)
            if (nrow(x) == 0) next
            cx <- ci(x$rel_dev)
            lab <- if (est == "new") "biallelic" else "multiallelic-aware"
            emit("\n- %s: pooled relative bias %+.3f%% [%+.3f%%, %+.3f%%]",
                 lab, cx$mean, cx$lo, cx$hi)
        }
        emit("\n\nNB these pooled biases are the mean over replicates of each replicate's")
        emit(" FST ratio, matching the ANOVA's unit of analysis; that is a mean-of-ratios")
        emit(" and so sits slightly below the ratio-of-sums figures reported per cell")
        emit(" (Jensen). Both estimators are affected alike, so the comparison between them")
        emit(" is fair. Note the biallelic bias here is POSITIVE: measured against the")
        emit(" finite-sites estimand it sits high, because it is recovering the coalescent")
        emit(" FST (see section 5) -- it is not a broken estimator, it is a different one.\n")
        nsig_f <- sum(atf[["Pr(>F)"]][-rif] > 0.05, na.rm = TRUE)
        emit("\n%d / %d terms are non-significant at alpha = 0.05; the model explains %.2f%%",
             nsig_f, rif - 1, 100 * (ssf - atf[["Sum Sq"]][rif]) / ssf)
        emit(" of the variance in deviation-from-theory (residual, i.e. replicate noise =")
        emit(" %.1f%%).", 100 * atf[["Sum Sq"]][rif] / ssf)
        emit(" The estimator term is the cost of discarding multiallelic sites, measured")
        emit(" against the finite-sites estimand -- the same quantity the pi and dxy")
        emit(" analyses above report. Its effect size is what matters here: compare the")
        emit(" FST estimator term below with the pi and dxy ones above, where the same")
        emit(" exclusion costs double-digit percentages.\n")
    }
}

