##########
# 2c. Figure 3: estimator bias vs theory across the ploidy x missingness grid
##########
# reported as a simulation study (Morris, White & Crowther 2019), not a
# significance test. quantity of interest is the bias:
#   bias = mean(estimate) - E,   MCSE = SD(estimate)/sqrt(n_sim)
# MCSE shrinks like 1/sqrt(n_sim), so a t-test of "mean == E" mostly measures
# replicate count, not estimator quality. judge accuracy by relative-bias
# magnitude, report MCSE for context.
# Tajima's D special: its "theory" is a neutral simulation
# (analysis/calibrate_neutral_tajimaD.py) with its own MCSE, added in quadrature;
# E ~ 0 so bias is absolute. other four references are exact closed forms (ref MCSE = 0).
# combined ANOVA (below) asks the separate question of whether deviation depends
# on ploidy or missingness
emit("\n\n## 2c. Figure 3 - estimator bias vs theory (Monte Carlo SE)\n")
fig3_stats <- c("pi", "dxy", "fst_hudson", "thetaw", "tajimaD")
fig3_lab   <- c(pi = "pi", dxy = "dxy", fst_hudson = "Hudson FST",
                thetaw = "theta_W", tajimaD = "Tajima's D")
theo <- tryCatch(
    bind_rows(
        read_tsv(file.path(ANL, "missingness_summary.tsv"),
                 show_col_types = FALSE, progress = FALSE) |>
            select(stat, ploidy, miss_pct, estimator, theoretical),
        read_tsv(file.path(ANL, "thetaw_tajimaD_summary.tsv"),
                 show_col_types = FALSE, progress = FALSE) |>
            select(stat, ploidy, miss_pct, estimator, theoretical)),
    error = function(e) NULL)
per_rep <- tryCatch(
    bind_rows(
        read_tsv(file.path(ANL, "per_rep_missingness.tsv"),
                 show_col_types = FALSE, progress = FALSE),
        read_tsv(file.path(ANL, "per_rep_thetaw_tajimaD.tsv"),
                 show_col_types = FALSE, progress = FALSE)),
    error = function(e) NULL)

if (is.null(theo) || is.null(per_rep)) {
    emit("\n_(missing per_rep_*.tsv or *_summary.tsv; run analysis/03 & 07 first)_\n")
} else {
    fig3 <- per_rep |>
        filter(estimator == "new_multi", stat %in% fig3_stats) |>
        left_join(theo, by = c("stat", "ploidy", "miss_pct", "estimator")) |>
        mutate(resid = value - theoretical) |>
        filter(is.finite(resid))
    # Reference Monte Carlo SE. Only Tajima's D has a simulated reference (with
    # its own se_D); the other four references are exact closed forms (ref se = 0).
    ref_se <- tryCatch(
        read_tsv(file.path(ANL, "neutral_tajimaD_reference.tsv"),
                 show_col_types = FALSE, progress = FALSE) |>
            transmute(stat = "tajimaD", ploidy, miss_pct, ref_se = se_D),
        error = function(e) NULL)

    # Per-cell bias and its Monte Carlo SE. bias = mean(estimate) - E; the sim-side
    # MCSE is SD/sqrt(n_sim); for Tajima's D the reference MCSE adds in quadrature.
    cells <- fig3 |>
        group_by(stat, ploidy, miss_pct) |>
        summarise(mean = mean(value), sd = sd(value), n = dplyr::n(),
                  E = first(theoretical), .groups = "drop") |>
        mutate(se_sim = sd / sqrt(n), bias = mean - E, rel_bias = 100 * bias / E)
    if (!is.null(ref_se)) cells <- cells |> left_join(ref_se, by = c("stat", "ploidy", "miss_pct"))
    if (!"ref_se" %in% names(cells)) cells$ref_se <- 0
    cells <- cells |> mutate(ref_se = ifelse(is.na(ref_se), 0, ref_se),
                             mcse = sqrt(se_sim^2 + ref_se^2))

    emit("\nMultiallelic-aware estimator (the one plotted in Figure 3) for pi, dxy, theta_W")
    emit(" and Tajima's D. **Hudson FST is the exception: it is biallelic-only.** These")
    emit(" missingness arms ran before the 2026-07 FST fix, when pixy's FST path re-filtered")
    emit(" to biallelic sites regardless of --include_multiallelic_snps, so the `new_multi`")
    emit(" FST column here holds the biallelic estimand. That is the right target for")
    emit(" Figure 3 regardless -- biallelic FST recovers the coalescent ratio 1 - T_W/T_B,")
    emit(" which is the flat E[FST] tested here -- but 'multiallelic-aware' does not mean")
    emit(" the same thing for FST as for the other four statistics.")
    emit(" bias = mean(estimate)")
    emit(" - E, pooled over the whole grid; MCSE = SD/sqrt(n_sim) is the Monte Carlo")
    emit(" precision of that bias (Morris, White & Crowther 2019), NOT a threshold the")
    emit(" estimator must beat. Accuracy is read off the relative-bias magnitude.")
    emit(" n ~ 72,000 replicates/statistic.\n")
    emit("\n| statistic | N_sim | bias (mean - E) | relative bias | MCSE | worst-cell bias |")
    emit("\n|---|---|---|---|---|---|")
    fig3_summ <- list()
    for (s in fig3_stats) {
        d  <- fig3 |> filter(stat == s)
        cd <- cells |> filter(stat == s)
        if (nrow(d) == 0) next
        near_zero_E <- s == "tajimaD"
        Emean  <- mean(d$theoretical)
        bias   <- mean(d$value) - Emean
        mcse   <- sqrt((sd(d$value) / sqrt(nrow(d)))^2 + mean(cd$ref_se)^2)
        # Tajima's D's expectation is ~0, so bias/E is ill-defined; use the same
        # +1 shift as the combined ANOVA (shift estimate and E by +1 -> the ratio
        # denominator is 1 + E), putting D on the same percentage scale as the rest.
        relb   <- if (near_zero_E) 100 * bias / (1 + Emean) else 100 * bias / abs(Emean)
        max_abs <- max(abs(cd$bias)); max_rel <- max(abs(cd$rel_bias))
        relcol <- if (near_zero_E) sprintf("%+.3f%% (+1 shift)", relb)
                  else sprintf("%+.3f%%", relb)
        maxcol <- if (near_zero_E) sprintf("%.4f abs", max_abs)
                  else sprintf("%.2f%% of E", max_rel)
        emit("\n| %s | %d | %+.2e | %s | %.1e | %s |",
             fig3_lab[s], nrow(d), bias, relcol, mcse, maxcol)
        fig3_summ[[s]] <- tibble(stat = fig3_lab[s], bias = bias, mcse = mcse,
                                 relb = relb, max_abs = max_abs, max_rel = max_rel,
                                 near_zero_E = near_zero_E)
    }
    fs <- bind_rows(fig3_summ)
    prop <- fs |> filter(!near_zero_E)
    emit("\n\nInterpretation (bias magnitude, per Morris et al. 2019):")
    emit("\n- pi, dxy, theta_W, and Hudson FST are unbiased to within %.2f%% of the expected value in every ploidy x missingness cell (pooled relative bias: %s).",
         max(abs(prop$max_rel)),
         paste(sprintf("%s %+.3f%%", prop$stat, prop$relb), collapse = ", "))
    drow <- fs |> filter(near_zero_E)
    if (nrow(drow))
        emit("\n- Tajima's D: pooled bias %+.4f (%+.3f%% under the +1 shift), worst-cell |bias| %.4f (absolute) against its simulated neutral reference; the per-window SD of D is ~1.6, so this is negligible.",
             drow$bias, drow$relb, drow$max_abs)
    emit("\n- MCSE (~1e-5 for pi/dxy/theta_W, ~1e-4 for FST) is the simulation's precision on the bias at this replicate count, not an accuracy bar; the biases above are the reportable quantities.\n")

    # single combined ANOVA: relative deviation ~ statistic x ploidy x missingness
    # (all fixed effects) over all five stats. relative deviation 100 * (est - E) / E
    # puts them on one scale; Tajima's D has E ~ 0 so a raw ratio blows up, apply a
    # +1 shift to both estimate and E (E_D ~ -0.03 -> ~0.97) before the ratio.
    # partitions deviation-from-theory by statistic, ploidy, missingness, interactions
    shift <- c(pi = 0, dxy = 0, fst_hudson = 0, thetaw = 0, tajimaD = 1)
    fig3_rel <- fig3 |>
        mutate(sft = shift[stat],
               rel_dev = 100 * ((value + sft) - (theoretical + sft)) / (theoretical + sft)) |>
        filter(is.finite(rel_dev)) |>
        mutate(statistic = factor(fig3_lab[stat], levels = unname(fig3_lab)),
               ploidy_f  = factor(ploidy),
               miss_f    = factor(miss_pct))
    aov_fit <- aov(rel_dev ~ statistic * ploidy_f * miss_f, data = fig3_rel)
    at <- summary(aov_fit)[[1]]
    ss_tot <- sum(at[["Sum Sq"]])
    terms <- trimws(rownames(at))
    pretty <- terms
    pretty <- gsub("ploidy_f", "ploidy", pretty)
    pretty <- gsub("miss_f", "missingness", pretty)
    pretty <- gsub(":", " x ", pretty)
    emit("\n\n**Combined ANOVA** - relative deviation from theory (%%) ~ statistic x ploidy x")
    emit(" missingness, all fixed effects, one model over all five statistics")
    emit(" (Tajima's D put on the percent scale via a +1 shift of estimate and E).")
    emit(" N = %d replicate estimates.\n", nrow(fig3_rel))
    emit("\n| term | df | F | p | %% of total SS |")
    emit("\n|---|---|---|---|---|")
    for (i in seq_along(terms)) {
        if (pretty[i] == "Residuals") next
        emit("\n| %s | %d | %.2f | %s | %.2f%% |",
             pretty[i], at[["Df"]][i], at[["F value"]][i],
             format.pval(at[["Pr(>F)"]][i], digits = 2),
             100 * at[["Sum Sq"]][i] / ss_tot)
    }
    miss_terms <- grep("missingness", pretty, value = TRUE)
    miss_ss <- sum(at[["Sum Sq"]][pretty %in% miss_terms])
    emit(paste0("\n\nEvery term is a fraction of a percent in effect size. Missingness",
                " (main effect + all interactions involving it) explains %.2f%% of the total",
                " variance in deviation-from-theory and is non-significant; the residual",
                " (replicate sampling noise) is %.1f%%. Ploidy and statistic are significant",
                " but explain a negligible share of the variance: no cell mean departs from",
                " its expectation by more than 0.5%%. (All five statistics share the same n,",
                " so significance here tracks effect size, not replicate count.)\n"),
         100 * miss_ss / ss_tot, 100 * at[["Sum Sq"]][terms == "Residuals"] / ss_tot)
    emit(paste0("\nFootnote on the `statistic` factor: it mixes four multiallelic-aware",
                " statistics (pi, dxy, theta_W, Tajima's D) with one biallelic one (Hudson",
                " FST -- see above). Each is compared against its own correct expectation, so",
                " the deviations are individually interpretable, but the significant",
                " `statistic` term is not an estimator comparison and must not be read as",
                " one.\n"))
}

##########
# 3. missingness x tool interaction on pi (pixy flat, vcftools collapses)
##########
emit("\n\n## 3. Missingness x tool interaction on pi (diploid)\n")
miss3 <- map_dfr(c("10", "25", "50", "75"), function(m) {
    d <- read_arm(paste0("pi_dip_1pop_miss", m))
    if (is.null(d) || !"pi_vcftools" %in% names(d)) return(NULL)
    d |> group_by(replicate) |>
        summarise(pixy = mean(pi_new, na.rm = TRUE),
                  vcftools = mean(pi_vcftools, na.rm = TRUE), .groups = "drop") |>
        mutate(miss = as.numeric(m) / 100)
})
if (nrow(miss3)) {
    long <- miss3 |> pivot_longer(c(pixy, vcftools), names_to = "tool", values_to = "pi")
    m <- lm(pi ~ miss * tool, data = long)
    co <- summary(m)$coefficients
    cf <- confint(m)
    # pixy slope = miss coef; vcftools slope = miss + miss:toolvcftools
    pixy_slope <- co["miss", "Estimate"]
    vcf_slope  <- co["miss", "Estimate"] + co["miss:toolvcftools", "Estimate"]
    emit("\nLinear model pi ~ missingness * tool (slopes per unit = per 100%% missingness):\n")
    emit("\n- pixy slope = %.4f [%.4f, %.4f] (flat)", pixy_slope, cf["miss", 1], cf["miss", 2])
    emit("\n- vcftools slope = %.4f (steep negative)", vcf_slope)
    emit("\n- interaction term miss:tool: estimate %.4f, p = %s\n",
         co["miss:toolvcftools", "Estimate"],
         format.pval(co["miss:toolvcftools", "Pr(>|t|)"], digits = 2))
    vmean <- miss3 |> group_by(miss) |> summarise(v = mean(vcftools), p = mean(pixy))
    emit("\nvcftools pi by missingness: %s. pixy pi: %s.\n",
         paste(sprintf("%.0f%%=%.3f", 100 * vmean$miss, vmean$v), collapse = ", "),
         paste(sprintf("%.0f%%=%.3f", 100 * vmean$miss, vmean$p), collapse = ", "))
}

##########
# 4. multiallelic bias grows with theta; aware estimator near-flat
##########
emit("\n\n## 4. Multiallelic bias vs theta sweep\n")
theta_codes <- c("005" = 0.005, "010" = 0.01, "025" = 0.025, "050" = 0.05, "100" = 0.1)
sweep <- map_dfr(names(theta_ploidy_files), function(k) {
    map_dfr(names(theta_codes), function(tc) {
        d <- read_arm(paste0("pi_", theta_ploidy_files[k], "_1pop_theta", tc))
        if (is.null(d)) return(NULL)
        th <- theta_codes[tc]
        Eth <- th / (1 + (4 / 3) * th)
        d |> group_by(replicate) |>
            summarise(bi = mean(pi_new, na.rm = TRUE),
