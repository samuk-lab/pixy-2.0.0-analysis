emit("\n\n## 7-8. Empirical biallelic vs multiallelic-aware\n")
empirical <- function(file, arm) {
    f <- file.path(EMP, file)
    if (!file.exists(f)) { emit("\n_(no data for %s)_\n", arm); return(invisible()) }
    d <- read_tsv(f, show_col_types = FALSE, progress = FALSE)
    pops <- sub("^avg_pi_(.+)_biallelic$", "\\1",
                grep("^avg_pi_.+_biallelic$", names(d), value = TRUE))
    emit("\n**%s** (populations: %s; %d windows):\n", arm, paste(pops, collapse = ", "), nrow(d))
    for (p in pops) {
        bi <- d[[paste0("avg_pi_", p, "_biallelic")]]
        mu_ <- d[[paste0("avg_pi_", p, "_multi")]]
        ok <- is.finite(bi) & is.finite(mu_) & bi > 0
        t <- t.test(mu_[ok], bi[ok], paired = TRUE)
        upl <- (mu_[ok] - bi[ok]) / bi[ok]
        cu <- ci(upl)
        # density proxy = multiallelic snps gained (fst snps multi - biallelic)
        dens <- NULL
        if (all(c("n_snps_fst_multi", "n_snps_fst_biallelic") %in% names(d)))
            dens <- (d$n_snps_fst_multi - d$n_snps_fst_biallelic)[ok]
        emit("\n- pi[%s]: paired mean uplift = %.5f [%.5f, %.5f], p = %s; relative uplift = %s [%s, %s]",
             p, t$estimate, t$conf.int[1], t$conf.int[2],
             format.pval(t$p.value, digits = 2), pct(cu$mean), pct(cu$lo), pct(cu$hi))
        if (!is.null(dens) && sd(dens) > 0) {
            sp <- suppressWarnings(cor.test(upl, dens, method = "spearman"))
            emit("; uplift~density Spearman rho = %.2f, p = %s",
                 unname(sp$estimate), format.pval(sp$p.value, digits = 2))
        }
    }
    emit("\n")
}
