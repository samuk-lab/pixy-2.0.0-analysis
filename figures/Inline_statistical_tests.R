out <- c()
emit <- function(...) out <<- c(out, sprintf(...))
read_arm <- function(name) {
    f <- file.path(AGG, paste0(name, ".tsv"))
    if (!file.exists(f)) return(NULL)
    suppressWarnings(read_tsv(f, show_col_types = FALSE, progress = FALSE))
}
# mean +/- 95% CI of a vector (t-based), returned as a one-row tibble
ci <- function(x) {
    x <- x[is.finite(x)]
    n <- length(x); m <- mean(x); se <- sd(x) / sqrt(n)
    tibble(mean = m, lo = m - qt(.975, n - 1) * se,
           hi = m + qt(.975, n - 1) * se, n = n)
}
pct <- function(x) sprintf("%+.2f%%", 100 * x)

